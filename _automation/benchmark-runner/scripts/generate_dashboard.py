"""
Reads _automation/benchmark-runner/runs/runs.json and generates DASHBOARD.md.

The dashboard shows:
- Score trend per eval (grouped by skill)
- Most recent run per eval + model
- Simple ASCII pass/fail indicator
"""

import json
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[3]
RUNS_FILE = REPO_ROOT / "_automation" / "benchmark-runner" / "runs" / "runs.json"
DASHBOARD_FILE = REPO_ROOT / "_automation" / "benchmark-runner" / "DASHBOARD.md"


def load_runs() -> list[dict]:
    if not RUNS_FILE.exists():
        return []
    with open(RUNS_FILE) as f:
        return json.load(f)


def generate_dashboard(runs: list[dict]) -> str:
    if not runs:
        return (
            "# Benchmark Dashboard\n\n"
            "_No benchmark runs recorded yet. "
            "Run `benchmark-runner` to populate this dashboard._\n"
        )

    lines = [
        "# Benchmark Dashboard",
        "",
        f"_Last generated: {datetime.utcnow().strftime('%Y-%m-%d %H:%M UTC')}_",
        "",
        "## Run History",
        "",
        "| Date | Eval ID | Skill | Model | SHA (short) | Status |",
        "|---|---|---|---|---|---|",
    ]

    for run in sorted(runs, key=lambda r: r.get("run_date", ""), reverse=True):
        sha_short = run.get("skill_sha", "unknown")[:8]
        run_date = run.get("run_date", "")[:10]
        lines.append(
            f"| {run_date} "
            f"| {run.get('eval_id', '-')} "
            f"| {run.get('skill_name', '-')} "
            f"| {run.get('model', '-')} "
            f"| `{sha_short}` "
            f"| {run.get('status', '-')} |"
        )

    # Summary by skill
    by_skill: dict[str, list[dict]] = defaultdict(list)
    for run in runs:
        by_skill[run.get("skill_name", "unknown")].append(run)

    lines += [
        "",
        "## Runs by Skill",
        "",
    ]
    for skill, skill_runs in sorted(by_skill.items()):
        lines.append(f"**{skill}**: {len(skill_runs)} total run(s)")

    lines += ["", "---", "*Generated by `generate_dashboard.py`*", ""]
    return "\n".join(lines)


def main() -> None:
    runs = load_runs()
    content = generate_dashboard(runs)
    DASHBOARD_FILE.write_text(content)
    print(f"Dashboard written to {DASHBOARD_FILE}")


if __name__ == "__main__":
    main()
